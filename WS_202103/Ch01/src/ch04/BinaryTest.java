package ch04;

public class BinaryTest {

	public static void main(String[] args) {

		int num = 10;
		int bNm = 0B1010;	// 2진수  0b 시작, b는 소문자 가능, 첫 0은 부호문자 
		int oNm = 012;		// 8진수  0 시작
		int xNm = 0XA;		// 16진수 0x 시작
		
		System.out.println(num);
		System.out.println(bNm);
		System.out.println(oNm);
		System.out.println(xNm);
		
		
	}

}
